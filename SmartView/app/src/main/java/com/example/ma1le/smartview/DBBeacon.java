public class DBBeacon {
    public String B_ID;
    public String B_NAME;
    public String B_X;
    public String B_Y;
    public String B_LAYER;

    public DBBeacon(String B_ID, String B_NAME, String B_X, String B_Y, String B_LAYER){
        this.B_ID = B_ID;
        this.B_NAME = B_NAME;
        this.B_X = B_X;
        this.B_Y = B_Y;
        this.B_LAYER = B_LAYER;
    }

}
